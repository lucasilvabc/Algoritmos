"""
Programação orientada a objetos - POO
- POO é uma paradigma de programação que utiliza mapeamento
 de objetos do mundo real para modelos computacionais.

- Paradigma de progamação é a forma/metodologia utilizada para
pensar/desenvolver sistemas.

Principais elementos da Orientação a objetos:
- Classe -> Modelo de objeto do mundo real sendo representado computacionalmente;
- Atributo -> Características do objeto.
- Método -> Comportamento do objeto (funções).
- Construtor -> Método especial utilizado para criar os objetos;
- Objeto -> Instância da classe.
"""
number = 10

print(number)
print(type(number))

name = "Geek"
print(name)
print(type(name))

class Produto:
    pass

ps4 = Produto()
print(ps4)
print(type(ps4))